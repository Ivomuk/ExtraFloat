def classify_agent_loan_status(df_pd: pd.DataFrame) -> pd.DataFrame:
    """
    Convert ``has_ever_loan`` (from repayment merge) into ``has_loan_history`` and
    ``is_new_agent`` binary flags.

    Args:
        df_pd: Modelling DataFrame; must contain ``has_ever_loan``.

    Returns:
        Copy of *df_pd* with ``has_loan_history`` and ``is_new_agent`` added.

    Raises:
        RuntimeError: If ``has_ever_loan`` is absent.
    """
    require_columns(df_pd, ["has_ever_loan"], context="classify_agent_loan_status")
    df = df_pd.copy()

    df["has_loan_history"] = df["has_ever_loan"].astype(int)

    assert set(df["has_loan_history"].unique()).issubset({0, 1}), (
        "has_loan_history contains values outside {0, 1}"
    )
    assert not df["has_loan_history"].isna().any(), (
        "has_loan_history contains NaNs"
    )

    df["is_new_agent"] = (df["has_loan_history"] == 0).astype(int)

    logger.info(
        "classify_agent_loan_status: %d agents with loan history, %d new agents",
        int(df["has_loan_history"].sum()),
        int(df["is_new_agent"].sum()),
    )
    return df


def add_thin_file_flags(
    df_pd: pd.DataFrame,
    cfg: ModelConfig = DEFAULT_CONFIG,
) -> pd.DataFrame:
    """
    Add ``thin_file_flag`` (1 = no loan history) and ``thin_file_pd_prior``
    (policy fallback PD, stored for reference but NOT used in training).

    Args:
        df_pd: Modelling DataFrame; must contain ``has_loan_history``.
        cfg:   Model config supplying ``thin_file_pd_prior``.

    Returns:
        Copy of *df_pd* with ``thin_file_flag`` and ``thin_file_pd_prior``.
    """
    require_columns(df_pd, ["has_loan_history"], context="add_thin_file_flags")
    df = df_pd.copy()

    df["thin_file_flag"] = (df["has_loan_history"] == 0).astype(int)
    df["thin_file_pd_prior"] = np.where(
        df["thin_file_flag"] == 1, cfg.thin_file_pd_prior, 0.0
    )

    n_thin = int(df["thin_file_flag"].sum())
    logger.info(
        "add_thin_file_flags: %d thin-file agents (thin_file_pd_prior=%.2f)",
        n_thin,
        cfg.thin_file_pd_prior,
    )
    return df


