def compute_bad_flags(df_pd: pd.DataFrame) -> pd.DataFrame:
    """
    Compute:
    - ``hard_bad_flag``  – short-term monitoring flag (repayment coverage + penalties)
    - ``bad_state``      – PD target (forward-looking 30-day bad indicator)

    ``bad_state_30D`` must already exist (generated upstream by SQL / data pipeline).

    Args:
        df_pd: Modelling DataFrame.

    Returns:
        Copy of *df_pd* with ``hard_bad_flag`` and ``bad_state`` added / refreshed.

    Raises:
        RuntimeError: If ``bad_state_30D`` is absent.
    """
    df = df_pd.copy()

    # Hard bad flag — monitoring signal
    repayment_coverage_1M = df.get("repayment_coverage_1M", pd.Series(1, index=df.index))
    penalties_1M = df.get("penalties_1M", pd.Series(0, index=df.index))
    repayment_val_1M = df.get("repayment_val_1M", pd.Series(1, index=df.index))

    df["hard_bad_flag"] = (
        (repayment_coverage_1M < 0.3)
        & (penalties_1M > 0)
        & (repayment_val_1M == 0)
    ).astype(int)

    # PD target — forward-looking 30-day
    if "bad_state_30D" not in df.columns:
        raise RuntimeError(
            "[compute_bad_flags] Missing required column 'bad_state_30D' "
            "(must be generated by the SQL pipeline before calling this function)."
        )

    df["bad_state"] = (df["bad_state_30D"].fillna(0) > 0).astype(int)

    assert set(df["bad_state"].unique()).issubset({0, 1}), (
        "bad_state contains values outside {0, 1}"
    )
    assert not df["bad_state"].isna().any(), "bad_state contains NaNs"

    logger.info(
        "compute_bad_flags: bad_state=%d/%d (%.2f%%), hard_bad_flag=%d",
        int(df["bad_state"].sum()),
        len(df),
        float(df["bad_state"].mean()) * 100,
        int(df["hard_bad_flag"].sum()),
    )
    return df


