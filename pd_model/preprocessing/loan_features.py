def leakage_audit_phase_2_2(
    df_pd: pd.DataFrame,
    snapshot_col: str = "snapshot_dt",
    txn_max_col: str = "max_txn_dt",
    label_cols: Tuple[str, ...] = (
        "bad_state", "current_dpd", "ever_written_off", "ever_in_collections"
    ),
    hard_fail: bool = True,
    cfg: ModelConfig = DEFAULT_CONFIG,
    verbose: bool = True,
) -> pd.DataFrame:
    """
    Run four leakage checks on the modelling DataFrame:

    1. **TARGET_LEAKAGE**   – label-related columns present in feature table.
    2. **TIME_LEAKAGE**     – transaction timestamps post-dating the snapshot.
    3. **LABEL_PROXY**      – features with |corr| > threshold against ``bad_state``.
    4. **BINARY_DOMINANCE** – binary columns where bad-rate jump exceeds threshold.

    Args:
        df_pd:        Modelling DataFrame.
        snapshot_col: Column holding the snapshot date.
        txn_max_col:  Column holding the maximum transaction date (may be absent).
        label_cols:   Columns considered target-adjacent.
        hard_fail:    If ``True``, raise ``RuntimeError`` on any HIGH finding.
        cfg:          Model config for correlation and dominance thresholds.
        verbose:      If ``True``, log the audit report.

    Returns:
        Audit report as a DataFrame with columns
        ``[check, column, severity, message]``.

    Raises:
        RuntimeError: On HIGH findings when ``hard_fail=True``.
    """
    if not isinstance(df_pd, pd.DataFrame):
        raise TypeError("df_pd must be a pandas DataFrame")

    audit_rows = []

    # ------------------------------------------------------------------ #
    # 1) Target leakage
    # ------------------------------------------------------------------ #
    for col_name in df_pd.columns:
        if col_name in label_cols and col_name != "bad_state":
            audit_rows.append(
                {
                    "check": "TARGET_LEAKAGE",
                    "column": col_name,
                    "severity": "HIGH",
                    "message": f"Label-related column present in feature table: {col_name}",
                }
            )

    # ------------------------------------------------------------------ #
    # 2) Future-time leakage
    # ------------------------------------------------------------------ #
    if snapshot_col in df_pd.columns and txn_max_col in df_pd.columns:
        snap_ser = df_pd[snapshot_col]
        txn_ser = df_pd[txn_max_col]

        if not pd.api.types.is_datetime64_any_dtype(snap_ser):
            snap_ser = pd.to_datetime(snap_ser, errors="coerce")
        if not pd.api.types.is_datetime64_any_dtype(txn_ser):
            txn_ser = pd.to_datetime(txn_ser, errors="coerce")

        future_mask = txn_ser.notna() & snap_ser.notna() & (txn_ser > snap_ser)
        if bool(future_mask.any()):
            n_future = int(future_mask.sum())
            audit_rows.append(
                {
                    "check": "TIME_LEAKAGE",
                    "column": txn_max_col,
                    "severity": "HIGH",
                    "message": f"{n_future} rows have transactions after snapshot date",
                }
            )

    # ------------------------------------------------------------------ #
    # 3) Label-proxy correlation
    # ------------------------------------------------------------------ #
    if "bad_state" in df_pd.columns:
        y = pd.to_numeric(df_pd["bad_state"], errors="coerce")
        if y.notna().sum() > 0:
            numeric_cols = [
                c
                for c in df_pd.columns
                if c != "bad_state" and pd.api.types.is_numeric_dtype(df_pd[c])
            ]
            if len(numeric_cols) > cfg.max_corr_cols:
                logger.warning(
                    "leakage_audit: limiting correlation sweep to first %d of %d numeric cols",
                    cfg.max_corr_cols,
                    len(numeric_cols),
                )
                numeric_cols = numeric_cols[: cfg.max_corr_cols]

            for col_name in numeric_cols:
                x = pd.to_numeric(df_pd[col_name], errors="coerce")
                valid_mask = x.notna() & y.notna()
                if int(valid_mask.sum()) < 50:
                    continue
                corr_val = x.loc[valid_mask].corr(y.loc[valid_mask])
                if pd.isna(corr_val):
                    continue
                abs_corr = float(abs(corr_val))
                if abs_corr > cfg.corr_high:
                    audit_rows.append(
                        {
                            "check": "LABEL_PROXY",
                            "column": col_name,
                            "severity": "HIGH",
                            "message": f"Very high correlation with bad_state: {corr_val:.3f}",
                        }
                    )
                elif abs_corr > cfg.corr_med:
                    audit_rows.append(
                        {
                            "check": "LABEL_PROXY",
                            "column": col_name,
                            "severity": "MEDIUM",
                            "message": f"High correlation with bad_state: {corr_val:.3f}",
                        }
                    )

    # ------------------------------------------------------------------ #
    # 4) Binary dominance
    # ------------------------------------------------------------------ #
    if "bad_state" in df_pd.columns:
        y = pd.to_numeric(df_pd["bad_state"], errors="coerce")
        if y.notna().sum() > 0:
            for col_name in df_pd.columns:
                if col_name == "bad_state":
                    continue
                ser = df_pd[col_name]
                if int(ser.dropna().nunique()) != 2:
                    continue
                vals = list(pd.Series(ser.dropna().unique()).sort_values())
                if len(vals) != 2:
                    continue
                v0, v1 = vals[0], vals[1]
                mask0, mask1 = ser == v0, ser == v1
                if int(mask0.sum()) < 30 or int(mask1.sum()) < 30:
                    continue
                rate_0 = float(y.loc[mask0].mean())
                rate_1 = float(y.loc[mask1].mean())
                jump = float(abs(rate_1 - rate_0))
                if np.isfinite(jump) and jump > cfg.dominance_high:
                    audit_rows.append(
                        {
                            "check": "BINARY_DOMINANCE",
                            "column": col_name,
                            "severity": "HIGH",
                            "message": (
                                f"Bad rate jump {jump:.3f} between "
                                f"{v0} and {v1}"
                            ),
                        }
                    )

    # ------------------------------------------------------------------ #
    # Report
    # ------------------------------------------------------------------ #
    audit_report = pd.DataFrame(audit_rows)

    if verbose:
        if not audit_report.empty:
            sev_order = {"HIGH": 0, "MEDIUM": 1, "LOW": 2}
            tmp = audit_report.copy()
            tmp["_ord"] = tmp["severity"].map(sev_order).fillna(9).astype(int)
            tmp = tmp.sort_values(["_ord", "check", "column"]).drop(columns=["_ord"])
            logger.warning("LEAKAGE AUDIT — %d findings:\n%s", len(tmp), tmp.to_string(index=False))
        else:
            logger.info("Leakage audit passed — no red flags detected")

    if hard_fail and not audit_report.empty:
        high_df = audit_report[audit_report["severity"] == "HIGH"].copy()
        if not high_df.empty:
            high_df = high_df.sort_values(["check", "column"])
            lines = ["HARD LEAKAGE DETECTED — FIX BEFORE MODELING"]
            lines.append(f"HIGH findings ({len(high_df)}):")
            for _, row in high_df.iterrows():
                lines.append(f"  - {row['check']} | {row['column']} | {row['message']}")
            raise RuntimeError("\n".join(lines))

    return audit_report


